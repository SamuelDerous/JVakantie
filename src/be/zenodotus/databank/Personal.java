package be.zenodotus.databank;


public class Personal
{

    private String adres;
    private String email;
    private int id;
    private String naam;
    private String telefoon;
    private String werkgever;
    private String wgAdres;
    private String wgEmail;
    private String wgTelefoon;

    public Personal()
    {
    }

    public String getAdres()
    {
        return adres;
    }

    public String getEmail()
    {
        return email;
    }

    public int getId()
    {
        return id;
    }

    public String getNaam()
    {
        return naam;
    }

    public String getTelefoon()
    {
        return telefoon;
    }

    public String getWerkgever()
    {
        return werkgever;
    }

    public String getWgAdres()
    {
        return wgAdres;
    }

    public String getWgEmail()
    {
        return wgEmail;
    }

    public String getWgTelefoon()
    {
        return wgTelefoon;
    }

    public void setAdres(String s)
    {
        adres = s;
    }

    public void setEmail(String s)
    {
        email = s;
    }

    public void setId(int i)
    {
        id = i;
    }

    public void setNaam(String s)
    {
        naam = s;
    }

    public void setTelefoon(String s)
    {
        telefoon = s;
    }

    public void setWerkgever(String s)
    {
        werkgever = s;
    }

    public void setWgAdres(String s)
    {
        wgAdres = s;
    }

    public void setWgEmail(String s)
    {
        wgEmail = s;
    }

    public void setWgTelefoon(String s)
    {
        wgTelefoon = s;
    }
}
