package be.zenodotus.databank;

public class Werkdag {
	
	private int id;
	private String dag;
	private boolean werkdag;
	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDag() {
		return dag;
	}
	public void setDag(String dag) {
		this.dag = dag;
	}
	public boolean isWerkdag() {
		return werkdag;
	}
	public void setWerkdag(boolean werkdag) {
		this.werkdag = werkdag;
	}

	
	
}
